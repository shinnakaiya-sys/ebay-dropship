# 米国向け固定サーチャージ（JPY）
US_SURCHARGE_CUSTOMS = 245   # 米国輸入通関手数料（/運送状）

# 規定外貨物判定しきい値
US_OVERSIZE_MAX_DIM_CM  = 55.88
US_OVERSIZE_MAX_VOL_CM3 = 55000
US_OVERSIZE_FEE         = 2475  # JPY

RATE_TABLES = {
    "US48":     _US48,
    "US_OTHER": _US_OTHER,
    "UK":       _UK,
    "DE":       _DE,
    "AU":       _AU,
}


def _lookup(table: dict, billed_weight_kg: float) -> int:
    """料金表から請求重量に対応する基本料金を返す（切り上げ検索）"""
    for limit, fee in sorted(table.items()):
        if billed_weight_kg <= limit:
            return fee
    # 上限超過: 最大料金を返す
    return table[max(table.keys())]


def calc_billed_weight(
    actual_weight_kg: float,
    length_cm: float = 0,
    width_cm: float = 0,
    height_cm: float = 0,
) -> float:
    """
    請求重量 = max(実重量, 容積重量)
    容積重量 = 長さ × 幅 × 高さ (cm) ÷ 8,000
    グラム端数は切り上げ（0.001kg単位）
    """
    vol_weight = (length_cm * width_cm * height_cm) / 8000 if (length_cm and width_cm and height_cm) else 0
    billed = max(actual_weight_kg, vol_weight)
    # 0.001kg単位で切り上げ
    return math.ceil(billed * 1000) / 1000


def get_shipping_jpy(
    weight_kg: float,
    destination: str = "US48",
    length_cm: float = 0,
    width_cm: float = 0,
    height_cm: float = 0,
) -> int:
    """
    SpeedPAK送料（JPY）を計算して返す

    Args:
        weight_kg:   実重量(kg)
        destination: "US48" / "US_OTHER" / "UK" / "DE" / "AU"
        length_cm / width_cm / height_cm: 寸法（容積重量計算用、省略可）

    Returns:
        送料合計（JPY）
    """
    table = RATE_TABLES.get(destination, _US48)
    billed = calc_billed_weight(weight_kg, length_cm, width_cm, height_cm)
    base   = _lookup(table, billed)

    # 米国固定サーチャージ
    surcharge = 0
    if destination in ("US48", "US_OTHER"):
        surcharge += US_SURCHARGE_CUSTOMS
        # 規定外貨物チェック（寸法がわかる場合）
        if length_cm or width_cm or height_cm:
            vol_cm3 = length_cm * width_cm * height_cm
            max_dim = max(length_cm, width_cm, height_cm)
            if max_dim > US_OVERSIZE_MAX_DIM_CM or vol_cm3 > US_OVERSIZE_MAX_VOL_CM3:
                surcharge += US_OVERSIZE_FEE

    return base + surcharge
